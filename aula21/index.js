/* 
Operadores logicos

&& = And
││ = Ou
! = Não

*/

console.log(true && true); // AND todas as expressao precisam ser verdadeira para retorna true
console.log(true || false); // Se uma expressao for verdadeira vai me retorna true
console.log(!true) // faz com que ele inverte e me retorna false com esse simbolo(!)